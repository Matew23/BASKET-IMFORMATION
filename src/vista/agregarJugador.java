    private void accion(String accion) {
        int op;
        String equipo = "";
        int opcionE = (int) jComboBoxEquipo.getSelectedIndex();
        int opcion = (int)jComboEstado.getSelectedIndex();
        System.out.println("opcion : "+opcion);
        if(opcion == 0){
            op = 1;
        }else{
            op = 0;
        }
        if(opcionE==0){
            equipo = "Los Angeles Lakers";
        }else if(opcionE==1){
            equipo = "Phoenix Suns";
        }else if(opcionE==2){
            equipo = "Golden State Warriors";
        }else if(opcionE==3){
            equipo = "Los Angeles Clippers";
        }
        if (NomComagretxt.getText().isEmpty() || altutxt1.getText().isEmpty() || nbashastxt.getText().isEmpty() || nbadtxt.getText().isEmpty() || numAnotxt2.getText().isEmpty()  || camisetatxt4.getText().isEmpty() ) {
            JOptionPane.showMessageDialog(this, "No dejar ningun campo vacio", "ERROR agregar", JOptionPane.ERROR_MESSAGE);
        } else {
           jugador ju = new jugador(Integer.parseInt(camisetatxt4.getText()) ,Integer.parseInt(nbadtxt.getText()) ,Integer.parseInt(nbashastxt.getText()), Double.parseDouble(altutxt1.getText()),Double.parseDouble(numAnotxt2.getText()), op, NomComagretxt.getText(),equipo);
            if (accion.equalsIgnoreCase("insertar")) {
                controller.agreg(ju);
                JOptionPane.showMessageDialog(null,"Jugador registrado","Jugador",JOptionPane.INFORMATION_MESSAGE);
                
            }
            if(accion.equalsIgnoreCase("actualizar")){
                try{
                    int idJugador = Integer.parseInt(jTable.getValueAt(jTable.getSelectedRow(), 0).toString());
                    ju.setIdJugador(idJugador);
                    controller.actualizar(ju);
                    JOptionPane.showMessageDialog(null,"Jugador actualizado","Jugador",JOptionPane.INFORMATION_MESSAGE);
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null,"Seleccione un jugador","Jugador",JOptionPane.ERROR_MESSAGE);
                }
                
            }

            GetTabla();
            CleanData();
        }

    }

    
