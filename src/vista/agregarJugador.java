    private void EliminarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarButtonActionPerformed
       try {
            int idJugador = Integer.parseInt(jTable.getValueAt(jTable.getSelectedRow(), 0).toString());
            boolean eliminar = controller.eliminarJuga(idJugador);
            if (eliminar) {
                JOptionPane.showMessageDialog(null, "Se elimino el Jugador");
                GetTabla();
            } else {
                JOptionPane.showMessageDialog(null,"Seleccione un jugador","Jugador",JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al eliminar el Jugador");
        }
    }//GEN-LAST:event_EliminarButtonActionPerformed
    private void GetTabla() {
        List<jugador> lista = controller.getDatosJugadores();
        DefaultTableModel dtm = (DefaultTableModel) jTable.getModel();
        dtm.setRowCount(0);
        DefaultTableModel modelo = (DefaultTableModel) jTable.getModel();
        for (int i = 0; i < lista.size(); i++) {
            String[] row = {String.valueOf(lista.get(i).getIdJugador()), lista.get(i).getNombre(), String.valueOf(lista.get(i).getNumero_A()), String.valueOf(lista.get(i).getAltura()), String.valueOf(lista.get(i).getNBA_desde()), String.valueOf(lista.get(i).getNBA_hasta()), String.valueOf(lista.get(i).getNumero_C()), String.valueOf(lista.get(i).getClub()), String.valueOf(lista.get(i).getisEstado_JG())};
            modelo.addRow(row);
        }
    }
   
